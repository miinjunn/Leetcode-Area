from typing import List


class Solution:
    def trimMean(self, arr: List[int]) -> float:
        arr.sort()
        # print(arr)
        n = len(arr)
        print(f'len awal: {n}')
        persen = int(0.05 * n)
        print(f'persen: {persen}')
        total = arr[persen:n-persen]
        print(f'total: {total}')

        print(f'sum: {sum(total)}')
        print(f'len: {len(total)}')
        result = sum(total) / len(total)
        # return float(f"{result:.6}")
        return result


